// Step 1: Define an array of favorite pizzas
let favorite_pizzas: string[] = ["Pepperoni", "Margherita", "BBQ Chicken"];

// Step 2: Print the name of each pizza
for (let pizza of favorite_pizzas) {
    console.log(pizza);
}

// Step 3: Print a sentence using the name of the pizza
for (let pizza of favorite_pizzas) {
    console.log(`I like ${pizza} pizza.`);
}

// Step 4: Add a line about how much you like pizza
console.log("I really love pizza!");
